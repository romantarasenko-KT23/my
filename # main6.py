from abc import ABC, abstractmethod
import math

class Shape(ABC):
    """–ê–±—Å—Ç—Ä–∞–∫—Ç–Ω–∏–π –∫–ª–∞—Å '–§—ñ–≥—É—Ä–∞'"""

    @abstractmethod
    def calculate_area(self):
        """–ê–±—Å—Ç—Ä–∞–∫—Ç–Ω–∏–π –º–µ—Ç–æ–¥ –¥–ª—è –æ–±—á–∏—Å–ª–µ–Ω–Ω—è –ø–ª–æ—â—ñ"""
        pass


# --- –ü—ñ–¥–∫–ª–∞—Å: –ö–æ–ª–æ ---
class Circle(Shape):
    def __init__(self, radius):
        self.radius = radius

    def calculate_area(self):
        return math.pi * self.radius ** 2


# --- –ü—ñ–¥–∫–ª–∞—Å: –ü—Ä—è–º–æ–∫—É—Ç–Ω–∏–∫ ---
class Rectangle(Shape):
    def __init__(self, width, height):
        self.width = width
        self.height = height

    def calculate_area(self):
        return self.width * self.height


# --- –ü—ñ–¥–∫–ª–∞—Å: –¢—Ä–∏–∫—É—Ç–Ω–∏–∫ ---
class Triangle(Shape):
    def __init__(self, base, height):
        self.base = base
        self.height = height

    def calculate_area(self):
        return 0.5 * self.base * self.height


# --- –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è –ø—ñ–¥–∫–ª–∞—Å—ñ–≤ ---
